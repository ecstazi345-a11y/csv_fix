# 7. Как LLM используется правильно
# ---------------------------------------------------------------------------
st.subheader("Как LLM используется правильно")

st.markdown(
    """
LLM **не считает** напрямую по IFC/PDF.  
LLM **переводит** человеческий вопрос в SQL/API-запрос к подготовленной базе.

**Пример.** Пользователь спрашивает:

> «Покажи все воздуховоды по титулу 16160-13, системе EF-002, которые попадают в июньский план.»

LLM формирует SQL-запрос к структурированной БД.  
Ответ строится на **проверенных данных**, а не на догадках модели.
"""
)

st.code(
    """SELECT
    e.element_id,
    e.mark,
    e.system_code,
    e.facility_building,
    m.boq_code,
    m.planned_qty,
    m.unit_of_measure
FROM rd_elements e
JOIN rd_boq_mapping m ON m.element_id = e.element_id
JOIN work_packages wp ON wp.package_id = m.package_id
WHERE e.facility_building = '16160-13'
  AND e.system_code = 'EF-002'
  AND wp.month_key = 'июнь-2026';""",
    language="sql",
)

