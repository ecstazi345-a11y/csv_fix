import streamlit as st

st.set_page_config(layout="wide")

st.title("Исполнимый фронт")
st.caption("Допуск работ к выполнению (Executability Gate)")

st.markdown("""
## Что показывает этот раздел

Этот раздел отвечает на вопрос:

**Можно ли физически выполнять работу сейчас?**

---

### Критерии допуска фронта

Работа считается допущенной к исполнению, если:

- есть утверждённая рабочая документация (РД)
- открыт фронт (доступ, зоны, пересечения сняты)
- обеспечены материалы и оборудование
- сформировано и мобилизовано звено
- отсутствуют блокирующие ограничения (HOLD)

---

### Управленческий смысл

Если работа выполняется без допуска:

- формируется хаос на площадке  
- возникает риск переделок  
- происходит неуправляемая загрузка ресурсов  
- снижается производительность  

---

### Ключевой принцип

**Нет допуска → нет работы**

Исполнимость фронта — это первый фильтр перед запуском СМР.
""")