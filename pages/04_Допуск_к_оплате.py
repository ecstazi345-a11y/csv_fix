st.caption("Допуск работ к признанию и конверсии в деньги (Acceptability Gate)")

st.markdown("""
## Что показывает этот раздел

Этот раздел отвечает на вопрос:

**Можно ли конвертировать выполненную работу в деньги?**

---

### Критерии допуска к оплате

Работа считается допустимой к признанию, если:

- она предусмотрена контрактом (BOQ / scope)
- согласованы изменения (Variation / DS)
- есть актуальная и утверждённая РД
- соблюдены требования HSE и качества
- есть возможность оформить исполнительную документацию
- результат может быть предъявлен заказчику

---

### Управленческий смысл

Если работа выполнена без допуска к оплате:

- она может не попасть в акты  
- не будет оплачена  
- создаёт убыток  
- формирует скрытые риски 
- формирует кассовые разрывы
- создает дефицит оборотного капитала

---

### Ключевой принцип

**Не всё, что построено — оплачивается**

Допуск к оплате — это фильтр между исполнением и деньгами.

---

### Связь с экономикой

Энергия звена (часы, люди, техника) должна конвертироваться:

**Труд → Объём → Признание → Деньги**

Если цепочка разрывается — возникает убыток.
""")
