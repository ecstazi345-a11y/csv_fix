import streamlit as st

st.set_page_config(layout="wide")

st.title("Приемка и признание")
st.caption("Подтверждение выполненных объемов и включение в оплату (Acceptance)")

st.markdown("""
## Что показывает этот раздел

Этот раздел отвечает на вопрос:

**Какие работы реально признаны заказчиком?**

---

### Суть процесса

После выполнения работ происходит:

- проверка объема
- проверка качества
- проверка соответствия РД
- формирование исполнительной документации
- предъявление заказчику
- фиксация замечаний (если есть)

---

### Результат

Работа:

- либо принимается (ACCEPTED)
- либо отклоняется / уходит в доработку

---

### Управленческий смысл

Разница между:

**выполнено → признано**

это:

- замороженные деньги  
- кассовый разрыв  
- риск споров  

---

### Ключевой принцип

**Работа становится деньгами только после признания**

---

### Контур управления

Исполнение → Приемка → Признание → Деньги

Если разрыв на этом этапе:

- работа есть  
- денег нет
""")