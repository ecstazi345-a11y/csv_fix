import streamlit as st

st.set_page_config(layout="wide")

st.title("Экономика")
st.caption(
    "Управленческая экономика проекта: труд, затраты, производительность и финансовый результат."
)

st.markdown(
    """
## Назначение раздела

Этот раздел показывает, во что превращается энергия проекта:

**люди → часы → объем → признание → деньги**

Экономика проекта рассматривается не как бухгалтерский отчет, а как система управленческого контроля.

## Что здесь анализируется

- прямые трудозатраты
- косвенные трудозатраты
- доля инженерно-технического персонала
- стоимость выполненного объема
- стоимость одного часа производительного труда
- потери от простоев и неэффективной загрузки
- разрыв между выполненным объемом и признанными деньгами
- финансовый результат по пакетам работ

## Управленческий смысл

Страница должна показывать:

- где труд не превращается в оплачиваемый объем
- где звенья расходуют часы без достаточного результата
- где управленческие и косвенные затраты начинают давить на проект
- какие пакеты работ ухудшают экономику проекта

## Ключевой принцип

**Не каждый отработанный час создает деньги.**

Задача системы — показать, где труд, техника и управление превращаются в результат, а где сгорают в потерях.
"""
)

c1, c2, c3, c4 = st.columns(4)
c1.metric("Прямые затраты", "—")
c2.metric("Косвенные затраты", "—")
c3.metric("Доля ИТР", "—")
c4.metric("Выработка на час", "—")

st.info("Экономическая витрина будет подключена после объединения трудозатрат, затрат и признанного объема.")
