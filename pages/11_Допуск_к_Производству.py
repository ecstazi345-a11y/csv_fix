import streamlit as st

st.set_page_config(layout="wide")

st.title("Допуск к производству")
st.caption(
    "Реестр исполнимости: что реально можно запускать в работу и что блокирует запуск."
)

st.markdown(
    """
## Что здесь делаем

Здесь мы смотрим управляемый допуск к производству.

Смысл страницы:
- не просто список пакетов
- а реальная картина исполнимости фронта

## Для чего нужна страница
Чтобы понимать:
- что можно запускать
- что заблокировано
- кто владелец ограничения
- что мешает выполнить план месяца
"""
)

col1, col2, col3 = st.columns(3)
col1.selectbox("Месяц", ["2026-04", "2026-05"])
col2.selectbox("Статус", ["Все", "READY", "AT RISK", "BLOCKED"])
col3.selectbox("Зона", ["Все"])

st.info("Позже сюда подключим витрину допуска и матрицу ограничений.")
